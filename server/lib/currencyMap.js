function getCurrencyForCountry(countryCode) {
  if (!countryCode || typeof countryCode !== 'string') {
    return null;
  }

  const upper = countryCode.toUpperCase();
  const currencyCode = COUNTRY_TO_CURRENCY[upper];
  if (!currencyCode) {
    return null;
  }

  return CURRENCIES[currencyCode] || null;
}

function getCurrencySymbol(code) {
  if (!code || typeof code !== 'string') {
    return null;
  }

  const upper = code.toUpperCase();
  const entry = CURRENCIES[upper];
  if (!entry) {
    return null;
  }

  return entry.symbol;
}

function getSubunitDigits(code) {
  if (!code || typeof code !== 'string') {
    return null;
  }

  const upper = code.toUpperCase();
  const entry = CURRENCIES[upper];
  if (!entry) {
    return null;
  }

  return entry.subunitDigits;
}

function getAllCurrencies() {
  const frequentSet = new Set(FREQUENT_CURRENCIES);
  const frequent = [];
  const rest = [];

  for (const [, entry] of Object.entries(CURRENCIES)) {
    if (!entry.supported) continue;

    const item = {
      code: entry.code,
      symbol: entry.symbol,
      name: entry.name,
    };
    if (frequentSet.has(entry.code)) {
      frequent.push(item);
    } else {
      rest.push(item);
    }
  }

  frequent.sort((a, b) => FREQUENT_CURRENCIES.indexOf(a.code) - FREQUENT_CURRENCIES.indexOf(b.code));
  rest.sort((a, b) => a.name.localeCompare(b.name));

  return [...frequent, ...rest];
}

